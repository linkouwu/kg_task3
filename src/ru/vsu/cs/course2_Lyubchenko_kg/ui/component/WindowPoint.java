package ru.vsu.cs.course2_Lyubchenko_kg.ui.component;

public class WindowPoint {
    private int x, y;

    public WindowPoint(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
